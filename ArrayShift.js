
// Shift will remove  the first element of an array


let data = [5,4,3,2,1];
console.log(data.shift());

console.log(data);


// UnShift it will add an element at the first of an array
 

let data = [5,4,3,2,1];
console.log(data.unshift(5));

console.log(data);

